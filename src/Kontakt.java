public class Kontakt {
    private String ime;
    private String broj;

    public Kontakt(String ime, String broj) {
        this.ime = ime;
        this.broj = broj;
    }

    public String getIme() {
        return ime;
    }

    public void setIme(String ime) {
        this.ime = ime;
    }

    public String getBroj() {
        return broj;
    }

    public void setBroj(String broj) {
        this.broj = broj;
    }
}
